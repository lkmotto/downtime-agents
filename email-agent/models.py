"""
Data models for the DownTime Weekend Email Digest Agent.

Extends the core Event model from the backend with email-specific grouping
and categorization structures.
"""

from dataclasses import dataclass, field
from typing import Optional
from pydantic import BaseModel, Field
from datetime import datetime


# ── Agent-side Event model ────────────────────────────────────────────────────
# The agent maintains its own Event model for email-specific fields
# (email_category, why_go). Backend models are imported from the
# downtime-backend package installed as a dependency.


class Event(BaseModel):
    """Mirror of the backend Event model — kept in sync manually."""

    id: str
    title: str
    description: str = ""
    category: str = ""
    scenario: str = ""
    source: str
    source_url: str = ""
    venue: str = ""
    address: str = ""
    city: str = ""
    state: str = ""
    lat: float = 0.0
    lon: float = 0.0
    date_start: Optional[str] = None
    date_end: Optional[str] = None
    time_info: str = ""
    price_range: str = ""
    price_note: str = ""
    image_url: Optional[str] = None
    camera_worthy: bool = False
    camera_note: Optional[str] = None
    tags: list[str] = Field(default_factory=list)
    score: int = 0
    is_featured: bool = False
    created_at: datetime = Field(default_factory=datetime.utcnow)

    # ── Email-only enrichment ──────────────────────────────────────────────────
    why_go: str = ""  # 1-2 sentence pitch generated by curator
    email_category: str = ""  # "Date Night", "Adventure/Outdoors", etc.


