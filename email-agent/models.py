@dataclass
class CuratedWeekend:
    """The final curated set of events for the weekend email."""

    fetch_date: datetime
    weekend_start: str  # e.g. "Friday, Jan 17"
    weekend_end: str  # e.g. "Sunday, Jan 19"
    city_label: str  # e.g. "Dallas–Fort Worth"
    buckets: dict[str, list[Event]] = field(default_factory=dict)
    total_fetched: int = 0
    total_scored: int = 0

    @property
    def all_events(self) -> list[Event]:
        events = []
        for evts in self.buckets.values():
            events.extend(evts)
        return events
